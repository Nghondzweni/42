#include "libft.h"

int	ft_atoi(char const *str)
{
	int	i;
	int ngv;
	int nbr;

	i = 0;
	ngv = 0;
	nbr = 0;
	while ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\v')
			|| (str[i] == '\f') || (str[i] == '\r') || (str[i] == '\n'))
		i++;
return(0);
}
